package com.mdream.lyservices.model.game;

public class GameObject {
	
	private String gameid;
	
	private String gamename;
	
	private String gameurl;
	
	private String gameicon;
	
	private String gamestar;
		
	private String hotflag;
	
	private String recommendflag;
	
	private String godflag;
	
	private String nowflag;
	
	private String flag;

	public String getGameid() {
		return gameid;
	}

	public void setGameid(String gameid) {
		this.gameid = gameid;
	}

	public String getGamename() {
		return gamename;
	}

	public void setGamename(String gamename) {
		this.gamename = gamename;
	}

	public String getGameurl() {
		return gameurl;
	}

	public void setGameurl(String gameurl) {
		this.gameurl = gameurl;
	}

	public String getGameicon() {
		return gameicon;
	}

	public void setGameicon(String gameicon) {
		this.gameicon = gameicon;
	}

	public String getGamestar() {
		return gamestar;
	}

	public void setGamestar(String gamestar) {
		this.gamestar = gamestar;
	}

	public String getHotflag() {
		return hotflag;
	}

	public void setHotflag(String hotflag) {
		this.hotflag = hotflag;
	}

	public String getRecommendflag() {
		return recommendflag;
	}

	public void setRecommendflag(String recommendflag) {
		this.recommendflag = recommendflag;
	}

	public String getGodflag() {
		return godflag;
	}

	public void setGodflag(String godflag) {
		this.godflag = godflag;
	}

	public String getNowflag() {
		return nowflag;
	}

	public void setNowflag(String nowflag) {
		this.nowflag = nowflag;
	}

	public String getFlag() {
		return flag;
	}

	public void setFlag(String flag) {
		this.flag = flag;
	}
		
	
}
